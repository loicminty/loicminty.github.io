# 1. Create a folder and put your index.html inside
mkdir sleepcell
cd sleepcell
# copy your index.html here

# 2. Initialize git
git init

# 3. Add your file
git add index.html

# 4. Commit
git commit -m "Initial commit - Sleep Cell portfolio"

# 5. Connect to GitHub (replace YOURUSERNAME with your actual username)
git remote add origin https://github.com/YOURUSERNAME/sleepcell.git

# 6. Push to GitHub
git branch -M main
git push -u origin main